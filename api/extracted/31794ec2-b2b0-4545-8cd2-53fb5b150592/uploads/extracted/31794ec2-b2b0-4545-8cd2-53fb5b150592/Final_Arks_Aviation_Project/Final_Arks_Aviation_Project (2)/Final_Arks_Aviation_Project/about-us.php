    <script>
      // Initialize AOS animation library
      document.addEventListener("DOMContentLoaded", function () {
        AOS.init();

        // Add scroll animation for elements
        const animateOnScroll = function () {
          const infoCards = document.querySelectorAll(".info-card");
          infoCards.forEach((card, index) => {
            setTimeout(() => {
              card.classList.add("animate_animated", "animate_fadeInUp");
            }, index * 200);
          });
        };

        // Trigger animations when scrolled into view
        const observer = new IntersectionObserver((entries) => {
          entries.forEach((entry) => {
            if (entry.isIntersecting) {
              animateOnScroll();
              observer.unobserve(entry.target);
            }
          });
        });

        const aboutSection = document.querySelector(".about-section");
        if (aboutSection) {
          observer.observe(aboutSection);
        }
      });

      // Image Section js
      function openPopup(src) {
        document.getElementById("popupImg").src = src;
        document.getElementById("popup").style.display = "flex";
      }

      function closePopup() {
        document.getElementById("popup").style.display = "none";
      }

      const container1 = document.getElementById("img-container1");
      container1.addEventListener("wheel", (event) => {
        if (event.deltaY !== 0) {
          event.preventDefault();
          container1.scrollLeft += event.deltaY; // Scroll horizontally with vertical scroll
        }
      });
      const container2 = document.getElementById("img-container2");
      container2.addEventListener("wheel", (event) => {
        if (event.deltaY !== 0) {
          event.preventDefault();
          container2.scrollLeft += event.deltaY; // Scroll horizontally with vertical scroll
        }
      });
